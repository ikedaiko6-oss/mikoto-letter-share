function dateFromParts(data, prefix) {
  return `${data[`${prefix}Year`]}-${data[`${prefix}Month`].padStart(2, "0")}-${data[`${prefix}Day`].padStart(2, "0")}`;
}

function hashText(text) {
  let hash = 0;
  for (let i = 0; i < text.length; i += 1) {
    hash = (hash << 5) - hash + text.charCodeAt(i);
    hash |= 0;
  }
  return Math.abs(hash);
}

function pickPart(list, seed, salt = 0) {
  return list[Math.floor(seed / (salt + 1)) % list.length];
}

function buildOmikuji(seed, data, worry) {
  const signalSeed = hashText(`${data.nickname}-${data.birthYear}-${data.birthMonth}-${data.birthDay}-${data.theme}-${data.concern}-${worry}`);
  return {
    name: pickPart(omikujiParts.names, signalSeed, 2),
    focusLead: pickPart(omikujiParts.focusLead, signalSeed, 3),
    bridge: pickPart(omikujiParts.insightBridge, signalSeed, 5),
    letterLead: pickPart(omikujiParts.letterLead, signalSeed, 7),
    letterClose: pickPart(omikujiParts.letterClose, signalSeed, 11),
    light: pickPart(omikujiParts.lights, signalSeed, 13),
    adviceLead: pickPart(omikujiParts.adviceLead, signalSeed, 17),
    sealedLead: pickPart(omikujiParts.sealedLead, signalSeed, 19),
    scoreOffset: signalSeed % 5
  };
}

function getWorryInsight(worry, seed) {
  if (!worry) return "";
  const matched = worrySignals.filter((signal) =>
    signal.words.some((word) => worry.includes(word))
  );

  if (matched.length > 0) {
    return matched[seed % matched.length].line;
  }

  const fallback = [
    `「${worry}」と書いた時点で、答えそのものより先に、止まっている理由を言語化したい段階に入っています。`,
    `この入力には、すぐ解決したい気持ちと、雑に決めたくない気持ちが同時に出ています。`,
    `いま必要なのは大きな決断より、頭の中で何度も戻ってくる一点を外に出すことです。`
  ];
  return fallback[seed % fallback.length];
}

