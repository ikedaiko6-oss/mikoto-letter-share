function getSunSign(dateText) {
  const date = new Date(`${dateText}T00:00:00`);
  const month = date.getMonth() + 1;
  const day = date.getDate();
  let current = signs[0];
  signs.forEach((sign) => {
    const [fromMonth, fromDay] = sign.from;
    if (month > fromMonth || (month === fromMonth && day >= fromDay)) {
      current = sign;
    }
  });
  return current;
}

