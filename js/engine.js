function softenResultTone(text) {
  if (!text) return "";
  return text
    .replace(/後回しにしています/g, "後回しになりやすいようです")
    .replace(/見落としています/g, "見えにくくなっているようです")
    .replace(/言えなくなっています/g, "言いにくくなっているようです")
    .replace(/信じきれていません/g, "信じるのが少し難しくなっているようです")
    .replace(/疑い始めています/g, "疑いやすくなっているようです")
    .replace(/気づいています/g, "薄々感じているようです")
    .replace(/迎えに行けていません/g, "まだ迎えに行く途中にいるようです")
    .replace(/なくなっています/g, "少なくなっているようです")
    .replace(/しまっています/g, "しまいやすいようです")
    .replace(/しまいます/g, "しまいやすくなります")
    .replace(/減っていきます/g, "減っていきやすくなります")
    .replace(/増えていきます/g, "増えていきやすくなります")
    .replace(/ています。/g, "ているようです。");
}

